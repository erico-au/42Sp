int		ft_strcmp(char *s1, char *s2)
{
	int x;

	x = 0;
	while (s1[x])
	{
		if (s1[x] != s2[x])
			return (*s1 - *s2);
		s1++;
		s2++;
	}
	if (s1 == s2)
		return (0);
	else
		return (-*s2);
}
