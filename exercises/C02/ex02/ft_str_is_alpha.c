int		ft_str_is_alpha(char *str)
{
	int x;
	int y;

	x = 0;
	y = 0;
	while (str[x] != '\0')
	{
		if (!((str[x] >= 'a' && str[x] <= 'z') ||
			(str[x] >= 'A' && str[x] <= 'Z')))
		{
			return (0);
			y = 1;
		}
		x++;
	}
	if (y == 0)
		return (1);
}
