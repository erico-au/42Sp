int		ft_str_is_printable(char *str)
{
	int x;
	int y;

	x = 0;
	y = 0;
	while (str[x] != '\0')
	{
		if (((str[x] >= 0 && str[x] <= 31) || str[x] == 127))
		{
			return (0);
			y = 1;
		}
		x++;
	}
	if (y == 0)
		return (1);
}
