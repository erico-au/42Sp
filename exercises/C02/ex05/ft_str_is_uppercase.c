int		ft_str_is_uppercase(char *str)
{
	int x;
	int y;

	x = 0;
	y = 0;
	while (str[x] != '\0')
	{
		if (!(str[x] >= 'A' && str[x] <= 'Z'))
		{
			return (0);
			y = 1;
		}
		x++;
	}
	if (y == 0)
		return (1);
}
